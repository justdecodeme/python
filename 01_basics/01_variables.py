# 🔤 Variables: Strings, Integers, Floats, Booleans
# Define your variables below! 👇

my_str = "Hello 🌍"
my_int = 42
my_float = 3.14
my_bool = True

print("...........")
print(my_str) # Hello 🌍
print(my_int) # 42
print(my_float) # 3.14
print(my_bool) # True

print("...........")
print(type(my_str)) # <class 'str'>
print(type(my_int)) # <class 'int'>
print(type(my_float)) # <class 'float'>
print(type(my_bool)) # <class 'bool'>
